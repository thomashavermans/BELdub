// AANMAKEN VAN MARKERS VIA DATA VAN ARRAYS

function createMarker(map, data, icon) {
  var modal = document.getElementsByClassName('modalmap')[0];
  var havermans = document.getElementsByTagName('body');
  for (var i = 0; i < data.length; i++) {
    var marker = new google.maps.Marker({
        id: data[i].id,
        info: data[i].info,
        artist: data[i].artiest,
        artistimg: data[i].artiestimg,
        website: data[i].website,
        weblink: data[i].weblink,
        map: map,
        position: {
          lat: Number(data[i].lat),
          lng: Number(data[i].lng),
        },
        icon: {
          url: icon,
          scaledSize: new google.maps.Size(30, 30),
        }
    });

    // OPENEN VAN MODAL MET CONTENT DATA MARKER

    google.maps.event.addDomListener(marker, 'click', function showmodal() {
      modal.style.opacity = 1;
      modal.style.visibility = 'visible';
      // template maken voor modal met data van marker
      document.getElementById('name').innerHTML = this.artist;
      document.getElementById('artistimg').style.backgroundImage = "url(http://"+this.artistimg+")";
      document.getElementById('filter').innerHTML = this.id;
      document.getElementById('about').innerHTML = "Over " + this.artist;
      document.getElementById('info').innerHTML = this.info;
      document.getElementById('website').innerHTML = this.website;
      document.getElementById('website').href = "http://" + this.weblink;
    });

    // verwijderen van modal wanneer er naast modal wordt geklikt
    window.onclick = function(event) {
        if (event.target == modal) {
          modal.style.opacity = 0;
          modal.style.visibility = 'hidden'
        }
    }
    arr.push(marker);
  }
}

// VERWIJDEREN VAN MARKERS via checkbox

function deletemarker(id) {
    var teverwijderen = [id];

    for(var i = 0; i < arr.length; i++) {
        var obj = arr[i];
        if(teverwijderen.indexOf(obj.id) !== -1) {
            obj.setMap(null)
            arr.splice(i, 1);
            i--;
        }
    }
};

